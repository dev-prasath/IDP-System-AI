# =========================================================
# IMPORTS
# =========================================================

import re

# =========================================================
# CREATE ENTITY
# =========================================================

def create_entity(
    label,
    text,
    confidence=0.99
):

    return {

        "label": label,

        "text": str(text).strip(),

        "confidence": confidence,

        "source": "regex"
    }

# =========================================================
# REGEX PATTERNS
# =========================================================

REGEX_PATTERNS = {

    # =====================================================
    # EMAIL
    # =====================================================

    "EMAIL": [

        r'\b[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Z|a-z]{2,}\b'
    ],

    # =====================================================
    # PHONE
    # =====================================================

    "PHONE": [

        r'(?:\+91[\-\s]?)?[6-9]\d{9}\b'
    ],

    # =====================================================
    # DATE
    # =====================================================

    "DATE": [

        r'\b\d{2}/\d{2}/\d{4}\b',

        r'\b\d{2}-\d{2}-\d{4}\b'
    ],

    # =====================================================
    # GST
    # =====================================================

    "GST": [

        r'\b\d{2}[A-Z]{5}\d{4}[A-Z]{1}[A-Z\d]{1}[Z]{1}[A-Z\d]{1}\b'
    ],

    # =====================================================
    # PAN
    # =====================================================

    "PAN": [

        r'\b[A-Z]{5}[0-9]{4}[A-Z]{1}\b'
    ],

    # =====================================================
    # AADHAAR
    # =====================================================

    "AADHAAR": [

        r'\b\d{4}\s\d{4}\s\d{4}\b'
    ],

    # =====================================================
    # INVOICE NUMBER
    # =====================================================

    "INVOICE_NUMBER": [

    r'(?:Invoice\s*(?:No|Number|#|No.)\s*[:\-]?\s*)([A-Z0-9\-]{3,})'
    ],

    # =====================================================
    # TOTAL AMOUNT
    # =====================================================

    "AMOUNT": [

        # Currency amounts only

        r'(?:₹|\$|Rs\.?)\s?\d[\d,]*\.\d{2}',

        # Large decimal amounts only

        r'\b\d{1,3}(?:,\d{3})+\.\d{2}\b'
    ]
}

# =========================================================
# EXTRACT REGEX ENTITIES
# =========================================================

def extract_regex_entities(text):

    entities = []

    try:

        if not text:

            return []

        # =================================================
        # ITERATE PATTERNS
        # =================================================

        for label, patterns in REGEX_PATTERNS.items():

            for pattern in patterns:

                matches = re.finditer(

                    pattern,

                    text,

                    re.IGNORECASE
                )

                for match in matches:

                    # =====================================
                    # HANDLE GROUPS
                    # =====================================

                    if match.groups():

                        entity_text = match.group(1)

                    else:

                        entity_text = match.group()

                    entity_text = str(
                        entity_text
                    ).strip()

                    # =====================================
                    # SKIP SMALL JUNK
                    # =====================================

                    if len(entity_text) < 2:

                        continue

                    # =====================================
                    # CREATE ENTITY
                    # =====================================

                    entities.append(

                        create_entity(

                            label=label,

                            text=entity_text,

                            confidence=0.99
                        )
                    )

        return entities

    except Exception as e:

        print(
            f"Regex Extraction Error: {str(e)}"
        )

        return []